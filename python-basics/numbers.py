#  Program to add, subtract, multiply two number from input

first_number=int(input("enter the first number:"))
second_number=int(input("enter the second number:"))


sum_numbers = first_number + second_number
difference_numbers = first_number - second_number
quotient_numbers = first_number / second_number
multiply_numbers = first_number * second_number



print(f"The sum of the numbers : {sum_numbers}")
print(f"The difference of the numbers : {difference_numbers}")
print(f"The quotient of the numbers : {quotient_numbers}")
print(f"The product of the numbers : {multiply_numbers}")

