# program to print the sin, cos and tan of angles from -180 to 180 with a step of 30 degrees


import math
for x in range(-180,180,30):
    print("-------------------------------")
    print("angle(x)")
    print(math.sin(x), math.cos(x), math.tan(x))
    

  